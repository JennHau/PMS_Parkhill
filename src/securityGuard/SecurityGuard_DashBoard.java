    public void displaybarchart() {
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        dataset.setValue(sg.count_number_incident().pending, "Number of Incident", "PENDING");
        dataset.setValue(sg.count_number_incident().progess, "Number of Incident", "PROGRESSING");
        dataset.setValue(sg.count_number_incident().complete, "Number of Incident", "COMPLETE");

        JFreeChart chart = ChartFactory.createBarChart("Incident", "Incident Status", "Number", dataset, PlotOrientation.VERTICAL, false, true, false);
        CategoryPlot p = chart.getCategoryPlot();
        CategoryPlot categoryPlot = chart.getCategoryPlot();
        categoryPlot.setRangeGridlinePaint(Color.BLUE);
        categoryPlot.setBackgroundPaint(Color.WHITE);
        BarRenderer renderer = (BarRenderer) categoryPlot.getRenderer();
        Color clr3 = new Color(13, 50, 79);
        renderer.setSeriesPaint(0, clr3);

        ChartPanel frame = new ChartPanel(chart);

        jPanel5.removeAll();
        jPanel5.add(frame, BorderLayout.CENTER);
        jPanel5.validate();
    }
    
    

