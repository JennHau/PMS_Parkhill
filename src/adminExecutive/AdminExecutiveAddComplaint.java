    private void complainerCBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_complainerCBActionPerformed
        // TODO add your handling code here:
        if (complainerCB.getSelectedItem() != "-PLEASE SELECT-") {
            complaintDetailsTA.setEnabled(true);
        } else {
            complaintDetailsTA.setEnabled(false);
            complaintDetailsTA.setText("");
        }
    }//GEN-LAST:event_complainerCBActionPerformed

    private void unitNoCBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_unitNoCBActionPerformed
        // TODO add your handling code here:
        setComplainerCB();
        if (unitNoCB.getSelectedItem() != "-PLEASE SELECT-") {
            complainerCB.setEnabled(true);
        } else {
            complainerCB.setEnabled(false);
            complaintDetailsTA.setText("");
        }
    }//GEN-LAST:event_unitNoCBActionPerformed

    private void complaintDetailsTAKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_complaintDetailsTAKeyReleased
        // TODO add your handling code here:
        if (!complaintDetailsTA.getText().equals("")) {
            addBt.setEnabled(true);
        } else {
            addBt.setEnabled(false);
        }
    }//GEN-LAST:event_complaintDetailsTAKeyReleased
    
    private void setComplainerCB() {
        complainerCB.removeAllItems();
        complainerCB.addItem("-PLEASE SELECT-");
        List<String> availableComplainer = ae.extractAvailableComplainer(
                (String)unitNoCB.getSelectedItem());
        
        for (String complainers : availableComplainer) {
            complainerCB.addItem(complainers);
        }
    }
    
