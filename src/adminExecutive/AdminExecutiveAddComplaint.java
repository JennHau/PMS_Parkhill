    private void setUnitComboBox() {
        List<String> availableUnit = ae.extractAllProperties();
        
        for (int i = 0; i<availableUnit.size(); i++) {
            String[] unitDetails = availableUnit.get(i).split(";");
            String unitNo = unitDetails[0];
            unitNoCB.addItem(unitNo);
        }
    }
    
    private void addBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtActionPerformed
        // TODO add your handling code here:
        String complainerID = ((String)complainerCB.getSelectedItem()).substring(0, 9);
        String desc = complaintDetailsTA.getText();
        int result = JOptionPane.showConfirmDialog(null,"Are you sure to "
                        + "file this complaint?",
                        "FILE COMPLAINT",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);
            
                if(result == JOptionPane.YES_OPTION){
                    ae.fileComplaint(complainerID.toLowerCase(), desc);
                    JOptionPane.showMessageDialog (null, "Complaint has been filed!", 
                                    "FILE COMPLAINT", JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                    new AdminExecutiveComplaintManagement().setVisible(true);
                }
        
    }//GEN-LAST:event_addBtActionPerformed

