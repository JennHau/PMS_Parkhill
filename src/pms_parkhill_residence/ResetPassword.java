    private void confirmBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmBtActionPerformed
        String newPass = String.valueOf(newPasswordTF.getPassword());
        String confirmPass = String.valueOf(confirmPasswordTF.getPassword());
        try {
            // TODO add your handling code here:
        if (!"".equals(newPass) && !"".equals(confirmPass)) {
            if (newPass.equals(confirmPass)) {
                Validation vd = new Validation();
                boolean check = vd.passwordValid(String.valueOf(newPasswordTF.getPassword()));
                if(check) {
                    warningMessage.setText("");
                    Users users = new Users();
                    users.resetPassword(String.valueOf(newPasswordTF.getPassword()), email);
                    JOptionPane.showMessageDialog (null, "Your password has been "
                            + "changed successfully.\nPlease log in to your account with your latest password.", 
                            "RESET PASSWORD", JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                    new SignInPage().setVisible(true);
                } else {
                    warningMessage.setText("The password does not meet the minimum requirements!");
                }
            } else {
                warningMessage.setText("Passwords do not match!");
            }
        } else {
            warningMessage.setText("Please complete the form!");
        }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_confirmBtActionPerformed

