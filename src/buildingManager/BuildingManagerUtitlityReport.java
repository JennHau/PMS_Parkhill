    private void setTable() {
        DefaultTableModel tableModel = (DefaultTableModel)reportTable.getModel();
        tableModel.setRowCount(0);
        
        List<String> invoiceDetails = fh.fileRead("invoices.txt");
        
        for (int i=1; i<invoiceDetails.size(); i++) {
            String[] invoiceData = invoiceDetails.get(i).split(";");
            String unitNo = invoiceData[1];
            String type = invoiceData[2];
            String target = invoiceData[3];
            String consumption = invoiceData[4];
            String unit = invoiceData[5];
            String price = invoiceData[7];
            String period = invoiceData[8];
            
            if(type.equals(this.utilityType) && period.equals(this.monthYear)) {
                this.unit = unit;
                String[] tbData = {unitNo, target, consumption +" "+ unit, price};
                tableModel.addRow(tbData);
                totalPrice += Float.parseFloat(price);
                totalConsumption += Float.parseFloat(consumption);
            }
        } tConsumptionLabel.setText(BM.currencyFormat(totalConsumption) +" "+ unit);
        totalPriceLabel.setText("RM " + BM.currencyFormat(totalPrice));
                
    }
    
