    private void setTable() {
        DefaultTableModel tableModel = (DefaultTableModel)jTable1.getModel();
        tableModel.setRowCount(0);
        
        List<String> invoiceDetails = fh.fileRead("budgetAllocation.txt");
        
        for (int i=1; i<invoiceDetails.size(); i++) {
            String[] invoiceData = invoiceDetails.get(i).split(";");
            String allocationID = invoiceData[0].toUpperCase();
            String type = invoiceData[1];
            String desc = invoiceData[2];
            String budget = invoiceData[3];
            String year = invoiceData[4];
            
            if(year.equals(this.year)) {
                String[] tbData = {String.valueOf(i), allocationID, type, desc, budget};
                tableModel.addRow(tbData);
                totalPrice += Float.parseFloat(budget);
            }
        } totalBudgetLabel.setText("RM " + BM.currencyFormat(totalPrice));
                
    }
    
