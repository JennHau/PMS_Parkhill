    private void invoiceTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_invoiceTableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel tableModel = (DefaultTableModel)invoiceTable.getModel();
        int column = invoiceTable.getSelectedColumn();
        int row = invoiceTable.getSelectedRow();
        
        try {
            if (column == 5) {
            String feeType = String.valueOf(tableModel.getValueAt(row, 1));
            String target = String.valueOf(tableModel.getValueAt(row, 2));
            FileWriter fw = new FileWriter("currentInvoice.txt");
            BufferedWriter bw = new BufferedWriter(fw);
            fw.write(feeType +";"+ target +";");
            fw.flush(); bw.flush(); fw.close(); bw.close();
            new AccountExecutiveIssueInvoiceDetail(AE).setVisible(true);
            dispose();
            } 
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_invoiceTableMouseClicked

