    private void setTable() {
        DefaultTableModel tableModel = (DefaultTableModel)jTable1.getModel();
        tableModel.setRowCount(0);
        
        String status = (String)statusCB1.getSelectedItem();
        List<String> availableList = ae.extractFacilityBooking(facilityID, status);
        for (int i = 0; i < availableList.size(); i++) {
            String[] bookingDetails = availableList.get(i).split(";");
            String bookingID = bookingDetails[0];
            String facilityName = bookingDetails[2];
            String unitNo = bookingDetails[3];
            String date = bookingDetails[4];
            String startTime = bookingDetails[5];
            String endTime = bookingDetails[6];
            String payment = bookingDetails[7];
            
            String action;
            if(statusCB1.getSelectedItem().equals("UPCOMING")) {
                action = "MANAGE";
            } else if (statusCB1.getSelectedItem().equals("HISTORY") && !payment.equals("-")){
                action = "VIEW RECEIPT";
            } else {
                action = "-";
            }
            
            String[] tbData = {bookingID.toUpperCase(), facilityName, unitNo, date,
                startTime, endTime, payment, action};
            tableModel.addRow(tbData);
        }
    }

