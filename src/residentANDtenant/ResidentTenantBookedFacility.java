    private void bookedFacilityTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bookedFacilityTableMouseClicked
        // TODO add your handling code here:
        int column = bookedFacilityTable.getSelectedColumn();
        int row = bookedFacilityTable.getSelectedRow();
        
        if (column == 3) {
            String bookingID = String.valueOf(bookedFacTab.getValueAt(row, 0));
            String status = String.valueOf(bookedFacTab.getValueAt(row, column));
            
            if (status.equals("MODIFY")) {
                String date = String.valueOf(bookedFacTab.getValueAt(row, 2));
                String facilityID = RT.getFacilityId(bookingID);

                fb.Facility(facilityID);
                RT.toManageBookedFacility(RT, fb, bookingID.toLowerCase(), date);
                
                dispose();
            }
            else {
                RT.toFacilityReceipt(RT, bookingID);
            }
        }
    }//GEN-LAST:event_bookedFacilityTableMouseClicked

    private void searchTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchTextFieldKeyReleased
        // TODO add your handling code here:
        // reset table
        bookedFacTab.setRowCount(0);
        bookedFacilityTableSetUp();
        if (!"".equals(searchTextField.getText().toUpperCase())) {
            String module_code = searchTextField.getText().toUpperCase();
            for (int i=0; i<bookedFacTab.getRowCount(); i++) {
                // get module code from table
                String tmodule_code = String.valueOf(bookedFacilityTable.getValueAt(i, 1)).toUpperCase();
                // if module code not contain in search bar
                if (!tmodule_code.contains(module_code)) {
                    // remove module from table
                    bookedFacTab.removeRow(i);
                    i--;
                }
            }
        } 
        else {
            bookedFacTab.setRowCount(0);
            bookedFacilityTableSetUp();
        }
    }//GEN-LAST:event_searchTextFieldKeyReleased

